    def test_analyzer_initialization(self, mock_github_token, mock_anthropic_key):
        """Test that analyzer initializes all components correctly."""
        with (
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioFetcher"
            ) as mock_fetcher_class,
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioEvidenceExtractor"
            ) as mock_extractor_class,
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioInsightsGenerator"
            ) as mock_generator_class,
        ):
            analyzer = PortfolioAnalyzer(mock_github_token, mock_anthropic_key)

            # Verify fetcher was initialized with GitHub token
            mock_fetcher_class.assert_called_once_with(mock_github_token)

            # Verify evidence extractor was initialized
            mock_extractor_class.assert_called_once_with()

            # Verify insights generator was initialized with Anthropic key
            mock_generator_class.assert_called_once_with(mock_anthropic_key)

            # Verify analyzer has all components
            assert analyzer.fetcher == mock_fetcher_class.return_value
            assert analyzer.evidence_extractor == mock_extractor_class.return_value
            assert analyzer.insights_generator == mock_generator_class.return_value

    def test_analyze_portfolio_orchestration(
        self,
        mock_github_token,
        mock_anthropic_key,
        mock_fetch_result,
        mock_evidence,
        mock_insights_result,
    ):
        """Test that analyze_portfolio orchestrates all components correctly."""
        with (
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioFetcher"
            ) as mock_fetcher_class,
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioEvidenceExtractor"
            ) as mock_extractor_class,
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioInsightsGenerator"
            ) as mock_generator_class,
        ):
            # Setup mocks
            mock_fetcher = mock_fetcher_class.return_value
            mock_fetcher.fetch_user_portfolio.return_value = mock_fetch_result

            mock_extractor = mock_extractor_class.return_value
            mock_extractor.extract_all_evidence.return_value = mock_evidence

            mock_generator = mock_generator_class.return_value
            mock_generator.generate_insights.return_value = mock_insights_result

            # Create analyzer and run analysis
            analyzer = PortfolioAnalyzer(mock_github_token, mock_anthropic_key)
            result = analyzer.analyze_portfolio(
                username="testuser",
                context="enterprise",
                tier="professional",
                max_repos=100,
                role="senior",
            )

            # Verify fetcher was called correctly
            mock_fetcher.fetch_user_portfolio.assert_called_once_with("testuser", 100)

            # Verify evidence extractor was called with repos and username
            mock_extractor.extract_all_evidence.assert_called_once()
            call_args = mock_extractor.extract_all_evidence.call_args
            assert call_args[0][0] == mock_fetch_result.repos  # repos
            assert call_args[0][1] == "testuser"  # username

            # Verify insights generator was called with correct arguments
            mock_generator.generate_insights.assert_called_once()
            insights_call = mock_generator.generate_insights.call_args[1]
            assert insights_call["username"] == "testuser"
            assert insights_call["evidence"] == mock_evidence
            assert insights_call["repos"] == mock_fetch_result.repos
            assert insights_call["context"] == "enterprise"
            assert insights_call["tier"] == "professional"
            assert insights_call["role"] == "senior"

            # Verify top-level result structure
            assert result["success"] is True
            assert result["username"] == "testuser"
            assert result["context"] == "enterprise"
            assert result["model_used"] == "claude-3-5-sonnet-20241022"

            # Verify nested analysis result
            analysis_result = result["result"]
            assert analysis_result["summary"] == "Test portfolio summary"
            assert analysis_result["limitations"] == "PUBLIC REPOSITORIES ONLY"

            # Verify metadata was passed to insights generator and updated
            metadata = insights_call["metadata"]
            assert metadata.total_public_repos == 2
            assert metadata.repos_analyzed == 2

            # Verify metadata was updated with AI token usage and cost
            result_metadata = result["metadata"]
            assert result_metadata.tokens == 1500  # Updated after AI call
            assert result_metadata.cost == 0.015  # Updated after AI call

