    def test_analyze_portfolio_metadata_calculation(
        self,
        mock_github_token,
        mock_anthropic_key,
        mock_fetch_result,
        mock_evidence,
        mock_insights_result,
    ):
        """Test that metadata is calculated correctly from fetch result."""
        with (
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioFetcher"
            ) as mock_fetcher_class,
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioEvidenceExtractor"
            ) as mock_extractor_class,
            patch(
                "github_analyzer.data.portfolio_analyzer.PortfolioInsightsGenerator"
            ) as mock_generator_class,
        ):
            # Setup mocks
            mock_fetcher = mock_fetcher_class.return_value
            mock_fetcher.fetch_user_portfolio.return_value = mock_fetch_result

            mock_extractor = mock_extractor_class.return_value
            mock_extractor.extract_all_evidence.return_value = mock_evidence

            mock_generator = mock_generator_class.return_value
            mock_generator.generate_insights.return_value = mock_insights_result

            # Run analysis
            analyzer = PortfolioAnalyzer(mock_github_token, mock_anthropic_key)
            analyzer.analyze_portfolio(username="testuser")

            # Verify metadata was correctly built from fetch result
            insights_call = mock_generator.generate_insights.call_args[1]
            metadata = insights_call["metadata"]

            assert metadata.total_public_repos == mock_fetch_result.total_public_repos
            assert metadata.repos_analyzed == mock_fetch_result.repos_fetched
            assert metadata.repos_skipped == mock_fetch_result.repos_skipped
            assert len(metadata.analyzed_repos) == len(mock_fetch_result.repos)

            # Verify oldest/newest repo dates were calculated
            assert metadata.oldest_repo  # Should be set
            assert metadata.newest_repo  # Should be set

            # Verify tokens and cost updated after AI call
            assert metadata.tokens == mock_insights_result["total_tokens"]
            assert metadata.cost == mock_insights_result["cost"]

